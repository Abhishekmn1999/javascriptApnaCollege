// Artimetic Opertor
let a = 5;
let b = 2;

console.log("Artimetic Opertor");
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);
console.log("a ** b = ", a ** b); //a^b=25

// unary oprator
console.log("a = ", a, "b = ", b);
console.log("++a = ", ++a);
console.log("a =", a);



// asignment oprator
a += 5;
console.log("a = ", a);
